// Basic Types: Create variables of different types (i8, i32, f64, bool, char)
fn main() {
    let a: i8 = -45;
    let b: i16 = 1000;
    let c: f64 = 3.144445;
    let d: bool = true;
    let e: char = 'R';
    
    println!("Value of a: {}", a);
    println!("Value of b: {}", b);
    println!("Value of c: {}", c);
    println!("Value of d: {}", d);
    println!("Value of e: {}", e);
}
